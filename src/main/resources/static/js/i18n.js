/**
 * Translates the given key to the current language, falling back to HU for missing keys.
 * If the key does not exist there either, returns the key itself (so labels are never
 * left empty). The `vars` object values are used to replace placeholders in the text
 * using the `{name}` format.
 */
export function t(key, vars = {}) {
    const lang = I18N[state.language] ? state.language : 'hu';
    let text = I18N[lang][key] || I18N.hu[key] || key;
    Object.entries(vars).forEach(([name, value]) => {
        text = text.replaceAll(`{${name}}`, String(value));
    });
    return text;
}